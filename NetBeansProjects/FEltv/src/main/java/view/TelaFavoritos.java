    private void btnCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarActionPerformed
        String listaSelecionada = (String) cbxListas.getSelectedItem();
        if (listaSelecionada == null) return; // Se não tiver lista, não faz nada
        
        controller.ListaController controller = new controller.ListaController();
        java.util.List<String[]> videos = controller.listarVideosDaLista(listaSelecionada, model.Sessao.idUsuarioLogado);
        
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tabelaFavoritos.getModel();
        modelo.setColumnIdentifiers(new String[]{"ID", "Título", "Tipo"});
        modelo.setRowCount(0); // Limpa a tabela
        
        for (String[] v : videos) {
            modelo.addRow(v);
        }
    }//GEN-LAST:event_btnCarregarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linha = tabelaFavoritos.getSelectedRow();
        if (linha == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um vídeo para remover!");
            return;
        }
        
        int idVideo = Integer.parseInt(tabelaFavoritos.getValueAt(linha, 0).toString());
        String nomeLista = (String) cbxListas.getSelectedItem();
        
        controller.ListaController controller = new controller.ListaController();
        controller.removerVideo(nomeLista, idVideo, model.Sessao.idUsuarioLogado);
        
        javax.swing.JOptionPane.showMessageDialog(this, "Vídeo removido da lista!");
        btnCarregar.doClick(); 
    }//GEN-LAST:event_btnRemoverActionPerformed

