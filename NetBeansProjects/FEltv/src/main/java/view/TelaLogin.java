    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
     // 1. Pegar o que o usuário digitou nas caixas
        String login = jTextField1.getText();
        String senha = new String(jPasswordField1.getPassword());

        // 2. Chamar o Controller para verificar e pegar o ID
        controller.UsuarioController controller = new controller.UsuarioController();
        int idUsuario = controller.autenticar(login, senha);

        // 3. Tomar uma decisão
        if (idUsuario != -1) {
            // SALVA O USUÁRIO NA SESSÃO!
            model.Sessao.idUsuarioLogado = idUsuario; 
            
            javax.swing.JOptionPane.showMessageDialog(this, "Bem-vindo ao FEltv!");
            
            TelaPrincipal principal = new TelaPrincipal();
            principal.setVisible(true);
            this.dispose(); 
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuário ou senha incorretos!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

