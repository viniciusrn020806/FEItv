    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // 1. Pega o que foi digitado nas caixas
        String login = jTextField1.getText();
        String senha = new String(jPasswordField1.getPassword());
        
        // Verificação rápida para não cadastrar gente em branco
        if(login.isEmpty() || senha.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha usuário e senha para cadastrar!");
            return;
        }

        // 2. Chama o Controller para salvar no banco
        controller.UsuarioController controller = new controller.UsuarioController();
        boolean sucesso = controller.cadastrar(login, senha);

        // 3. Avisa o usuário
        if (sucesso) {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso! Agora você já pode clicar em Entrar.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro: Este usuário já existe no sistema!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

